package com.codigozerocuatro.taska.domain.model;

public enum TipoRecurrencia {
    DIARIA,    // Cada día
    SEMANAL,   // Cada semana, en un día específico de la semana
    QUINCENAL, // Cada dos semanas, en un día específico de la semana
    MENSUAL,   // Cada mes, en un día específico del mes
    UNA_VEZ    // Una sola vez, en una fecha específica
}
